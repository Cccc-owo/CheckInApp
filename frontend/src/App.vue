<template>
  <router-view />
</template>

<script setup>
import { onMounted } from 'vue'
import { useAuthStore } from '@/stores/auth'

const authStore = useAuthStore()

// 应用启动时验证 Token
onMounted(async () => {
  if (authStore.isAuthenticated) {
    try {
      await authStore.fetchCurrentUser()
    } catch (error) {
      console.error('验证用户信息失败:', error)
      // Token 可能已过期，清除认证状态
      authStore.clearAuth()
    }
  }
})
</script>

